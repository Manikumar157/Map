package com.lara;

public class Employee {
	private int Id;
	private String Name;
	public Employee() {
		System.out.println("hello");
	}
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		Id = id;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	
	

}
