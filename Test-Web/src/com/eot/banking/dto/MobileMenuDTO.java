package com.eot.banking.dto;

public class MobileMenuDTO {
	
	private Integer mobileMenuId;
	private Integer index;
	private Integer status;
	private Integer mobileMenuIconId;
	
	
	public Integer getMobileMenuId() {
		return mobileMenuId;
	}
	public void setMobileMenuId(Integer mobileMenuId) {
		this.mobileMenuId = mobileMenuId;
	}
	public Integer getIndex() {
		return index;
	}
	public void setIndex(Integer index) {
		this.index = index;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public Integer getMobileMenuIconId() {
		return mobileMenuIconId;
	}
	public void setMobileMenuIconId(Integer mobileMenuIconId) {
		this.mobileMenuIconId = mobileMenuIconId;
	}
	
	

}
